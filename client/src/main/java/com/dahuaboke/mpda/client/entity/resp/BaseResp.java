public class BaseResp {

    /**
     * 返回消息
     */
    private String msg;

    /**
     * 返回码
     */
    private String code;

    public String getMsg() {
        return msg;
    }

    public void setMsg(String msg) {
        this.msg = msg;
    }

    public String getCode() {
        return code;
    }

    public void setCode(String code) {
        this.code = code;
    }

}
